package edu.cca.john.jfs;

public class Demo {

	public static void main(String[] args) {
		/*
		== (Equal to)
		!= (Not equal to)
		> (Greater than)
		< (Less than)
		>= (Greater than or equal to)
		<= (Less than or equal to)
		*/
		int x = 10;
		int y = 20;
		
		System.out.println("Relational Operators");
		System.out.println(x == y); // false
		System.out.println(x != y); // true
		System.out.println(x < y);  // true
	}
}
